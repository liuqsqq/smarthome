/*-------------------------------- ------------------------------------------------------------------
** 函数名称： DelayNms
** 入口参数： count - 毫秒
** 函数功能：延时子程序，实现1ms的延时 
--------------------------------------------------------------------------------------------------*/
void DelayNms(u32 count)
{
     u32 i,j;
     for(i = count; i > 0 ; i--)
	 {
	    for(j = 110; j > 0; j--);
     }
}	


/*-------------------------------- ------------------------------------------------------------------
** 函数名称： DelayNus
** 入口参数： count - 微秒
** 函数功能：延时子程序，实现1us的延时 
--------------------------------------------------------------------------------------------------*/
void DelayNus(u32 count)
{
	while(count--);
}


/*-------------------------------- ------------------------------------------------------------------
** 函数名称： DelayNus
** 入口参数： count - 微秒
** 函数功能：延时子程序，实现1us的延时 
--------------------------------------------------------------------------------------------------*/
void Delay100us()
{
	DelayNus(13);
}
